CREATE TABLE fatura_artigos(
	id INT NOT NULL AUTO_INCREMENT,
	fatura_id INT NOT NULL,
	artigo_id INT NOT NULL,
	quantidade DECIMAL(15, 2) NOT NULL,
	valor DECIMAL(15, 2),
	PRIMARY KEY(id),
	FOREIGN KEY (fatura_id) REFERENCES faturas(id),
	FOREIGN KEY (artigo_id) REFERENCES classificacao(id)
);

